#include "libft.h"

void				ft_bzero(void *ptr, size_t n)
{
	unsigned char	*new_ptr;
	size_t			idx;

	new_ptr = ptr;
	idx = 0;
	while (idx < n)
		new_ptr[idx++] = 0;
}
